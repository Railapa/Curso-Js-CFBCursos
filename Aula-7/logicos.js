/* 
OPERADORES LOGICOS

E Lógico (&&)
Retorna true apenas se todas as condições forem verdadeiras. Se uma for falsa, o resultado final é falso.
Exemplo: (10 > 5) && (2 < 4) → true
Exemplo: (10 > 5) && (2 > 4) → false

OU Lógico (||)
Retorna true se pelo menos uma das condições for verdadeira. Só será falso se tudo for falso.
Exemplo: (10 > 5) || (2 > 4) → true (pois a primeira é real)
Exemplo: (10 < 5) || (2 > 4) → false

NÃO Lógico / Negação (!)
Inverte o valor booleano. O que é true vira false, e vice-versa.
Exemplo: !(10 > 5) → false (10 é maior que 5, mas o ! inverteu)
*/