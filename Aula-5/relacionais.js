/* 

OPERADORES RELACIONAIS

Operadores de Igualdade
Igualdade Ampla (==): Compara apenas o valor. Ele tenta converter os tipos antes de comparar (ex: 5 == "5" é true).
Igualdade Estrita (===): Compara o valor e o tipo. É o mais recomendado para evitar erros (ex: 5 === "5" é false).
Diferente (!=): Verifica se os valores são diferentes, convertendo tipos.
Diferente Estrito (!==): Verifica se os valores ou os tipos são diferentes.

Operadores de Grandeza
Maior que (>): true se o da esquerda for maior.
Menor que (<): true se o da esquerda for menor.
Maior ou igual (>=): true se o da esquerda for maior ou igual ao da direita.
Menor ou igual (<=): true se o da esquerda for menor ou igual ao da direita.

*/