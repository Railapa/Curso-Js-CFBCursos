/*
OPERADORES ARITIMETICOS:

BÁSICOS
Adição (+): Soma dois valores. Também é usado para concatenar (juntar) strings.
Subtração (-): Subtrai o valor da direita do valor da esquerda.
Multiplicação (*): Multiplica dois valores.
Divisão (/): Divide o primeiro valor pelo segundo.

COMPLEMENTARES 
Módulo (%): Retorna o resto da divisão. Muito usado para saber se um número é par ou ímpar (ex: 10 % 2 é 0).
Exponenciação (**): Eleva um número à potência de outro (ex: 2 ** 3 resulta em 8).

INCREMENTO E DECREMENTO
Incremento (++): Soma 1 à variável.
Decremento (--): Subtrai 1 da variável.
Nota: Eles podem ser usados antes (++x) ou depois (x++) da variável, o que muda o momento em que o valor é atualizado na expressão.

ORDEM DE PRECEDENCIA
Parênteses () sempre têm a maior prioridade.
Exponenciação **.
Multiplicação *, Divisão /, Módulo % (da esquerda para a direita).
Soma + e Subtração - (da esquerda para a direita).
*/